

public class RigthShift {

    public static void main(String[] args) {
        

        int a = 6;
        int b = 4;

        System.out.println(a>>b);  //  rightshift ( a >> b ) = a/2^b
        System.out.println(a<<b);  //  leftshift ( a << b ) = a*2^b


        System.out.println(a<<4);
    }

}
